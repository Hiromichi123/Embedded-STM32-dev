/**
	*	@brief		MAIN函数
	*	@param		无
	*	@retval		无
	*/
int main(void)
{
/**********************************************************
***	初始化板载外设
**********************************************************/
	board_init();

/**********************************************************
***	上电延时2秒等待Emm_V5.0闭环初始化完毕
**********************************************************/	
	delay_ms(2000);

/**********************************************************
***	设置当前位置为单圈回零的零点位置，不存储
**********************************************************/	
  Emm_V5_Origin_Set_O(1, 0);
	
/**********************************************************
***	等待返回命令，命令数据缓存在数组rxCmd上，长度为rxCount
**********************************************************/	
	while(rxFrameFlag == false); rxFrameFlag = false;

/**********************************************************
***	位置模式：方向CW，速度1000，加速度0（不使用加减速直接启动），脉冲数800（16细分下发送3200个脉冲电机转一圈），相对运动
**********************************************************/	
	Emm_V5_Pos_Control(1, 0, 1000, 0, 800, 0, 0);

/**********************************************************
***	等待返回命令，命令数据缓存在数组rxCmd上，长度为rxCount
**********************************************************/	
	while(rxFrameFlag == false); rxFrameFlag = false;

/**********************************************************
***	延时1秒，等待位置到达
**********************************************************/	
  delay_ms(1000);

/**********************************************************
***	运动完后，再发送命令触发单圈就近回零（注意：0为单圈就近回零，1为单圈方向回零，2为多圈无限位碰撞回零，3为多圈限位开关回零）
**********************************************************/	
  Emm_V5_Origin_Trigger_Return(1, 0, 0);

/**********************************************************
***	等待返回命令，命令数据缓存在数组rxCmd上，长度为rxCount
**********************************************************/	
	while(rxFrameFlag == false); rxFrameFlag = false;

/**********************************************************
***	WHILE循环
**********************************************************/	
	while(1)
	{
	}
}
