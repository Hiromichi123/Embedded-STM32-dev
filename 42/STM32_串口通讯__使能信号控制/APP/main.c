/**
	*	@brief		MAIN函数
	*	@param		无
	*	@retval		无
	*/
int main(void)
{
/**********************************************************
***	初始化板载外设
**********************************************************/
	board_init();

/**********************************************************
***	上电延时2秒等待Emm_V5.0闭环初始化完毕
**********************************************************/	
	delay_ms(2000);

/**********************************************************
***	位置模式：转速1000RPM，加速度0（直接启动），相对运动3200个脉冲（16细分下发送3200个脉冲，电机旋转一圈）
**********************************************************/	
  Emm_V5_Pos_Control(1, 0, 1000, 0, 3200, 0, 0);

/**********************************************************
***	等待返回命令，命令数据缓存在数组rxCmd上，长度为rxCount
**********************************************************/	
	while(rxFrameFlag == false); rxFrameFlag = false;

/**********************************************************
***	延时2秒，等待运动完成
**********************************************************/	
	delay_ms(2000);

/**********************************************************
***	使能信号控制 - 去使能
**********************************************************/	
	Emm_V5_En_Control(1, 0, 0);

/**********************************************************
***	等待返回命令，命令数据缓存在数组rxCmd上，长度为rxCount
**********************************************************/	
	while(rxFrameFlag == false); rxFrameFlag = false;

/**********************************************************
***	延时100毫秒
**********************************************************/	
	delay_ms(100);

/**********************************************************
***	使能信号控制 - 使能
**********************************************************/	
	Emm_V5_En_Control(1, 1, 0);

/**********************************************************
***	等待返回命令，命令数据缓存在数组rxCmd上，长度为rxCount
**********************************************************/	
	while(rxFrameFlag == false); rxFrameFlag = false;

/**********************************************************
***	WHILE循环
**********************************************************/	
	while(1)
	{
	}
}
